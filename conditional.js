/* if */
let age = 20;

if (age >= 18) {
  console.log("You can vote");
}
/* Example of if-else */
let number = 16;

if (number >= 18) {
  console.log("You can vote");
} else {
  console.log("You cannot vote");
}
/* Example of else if */
let marks = 75;

if (marks >= 90) {
  console.log("Grade A");
} else if (marks >= 60) {
  console.log("Grade B");
} else {
  console.log("Grade C");
}
